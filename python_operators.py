# Numeric operators in Python
# + for addition
# - for substract
# * for multipply
# / for float division
# // for int division
# ** for Power Calculation
# % for Modulus

x = 5
y = 3

print("Addition of x  + y =",x+y)
print("Subtract of x  - y =",x-y)
print("Multiply of x * y =",x*y)
print("Float Division of x / y =",x/y)
print("Int Division of x // y =",x//y)
print("Power of x ** y =",x**y)
print("Modulus of x % y =",x%y)

